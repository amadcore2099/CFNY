/* ---------- MOBILE DETECTION ---------- */

const isMobile =
  window.matchMedia("(max-width: 768px)").matches ||
  "ontouchstart" in window ||
  navigator.maxTouchPoints > 0;

/* ---------- HELPERS ---------- */

function getYouTubeId(url) {
  const match = url.match(/(?:v=|\/)([0-9A-Za-z_-]{11}).*/);
  return match ? match[1] : null;
}

function getEmbedUrl(id) {
  return `https://www.youtube-nocookie.com/embed/${id}?autoplay=1&playsinline=1&rel=0&modestbranding=1`;
}

function getThumbUrl(id) {
  return `https://i.ytimg.com/vi/${id}/hqdefault.jpg`;
}

/* ---------- RENDER ---------- */

function createCard(item) {
  const id = getYouTubeId(item.youtubeUrl);
  if (!id) return "";

  return `
  <div class="project-card youtube-card"
       data-video-id="${id}">
       
      <div class="project-thumb">
          <img src="${getThumbUrl(id)}" loading="lazy">
          <div class="project-overlay">
            <div class="play-button"><span>▶</span></div>
          </div>
      </div>

      <div class="project-info">
          <h3 class="project-title">${item.title}</h3>
          <p class="project-sub">${item.subtitle}</p>
      </div>
  </div>
  `;
}

/* ---------- VIDEO LIGHTBOX ---------- */

const videoLightbox = document.getElementById("videoLightbox");
const videoFrameWrap = document.getElementById("videoFrameWrap");

function openVideo(card) {
  const id = card.dataset.videoId;
  const embed = getEmbedUrl(id);

  document.body.classList.add("modal-open");

  videoLightbox.classList.add("is-open");

  videoFrameWrap.innerHTML = `
    <iframe
      src="${embed}"
      allow="autoplay; encrypted-media; picture-in-picture; fullscreen"
      allowfullscreen
      playsinline
    ></iframe>
  `;
}

function closeVideo() {
  videoFrameWrap.innerHTML = "";
  videoLightbox.classList.remove("is-open");
  document.body.classList.remove("modal-open");
}

/* ---------- BIND EVENTS ---------- */

function bindVideoCards() {
  document.querySelectorAll(".youtube-card").forEach(card => {

    /* tap delay fix */
    card.addEventListener("click", e => {
      e.preventDefault();
      openVideo(card);
    });

  });
}

/* ---------- RENDER PAGE ---------- */

function renderPage(pageKey) {

  const items = videoData[pageKey] || [];
  const grid = document.querySelector(".project-grid");

  if (!grid) return;

  grid.innerHTML = items.map(createCard).join("");

  bindVideoCards();
}

/* ---------- MOBILE MENU ---------- */

const menuToggle = document.getElementById("menuToggle");
const mobileMenu = document.getElementById("mobileMenu");

function openMobileMenu() {
  mobileMenu.classList.add("is-open");
  document.body.classList.add("modal-open");
}

function closeMobileMenu() {
  mobileMenu.classList.remove("is-open");
  document.body.classList.remove("modal-open");
}

if (menuToggle) {
  menuToggle.addEventListener("click", () => {
    mobileMenu.classList.contains("is-open")
      ? closeMobileMenu()
      : openMobileMenu();
  });
}

/* ---------- ESC KEY ---------- */

document.addEventListener("keydown", e => {
  if (e.key === "Escape") {
    closeVideo();
    closeMobileMenu();
  }
});

/* ---------- CLICK OUTSIDE ---------- */

if (videoLightbox) {
  videoLightbox.addEventListener("click", e => {
    if (e.target === videoLightbox) closeVideo();
  });
}

/* ---------- INIT ---------- */

const currentPage = document.body.dataset.page || "production";

if (["production", "marketing", "label"].includes(currentPage)) {
  renderPage(currentPage);
}
